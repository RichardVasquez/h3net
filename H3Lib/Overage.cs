namespace H3Lib
{
    /// <summary>
    /// Digit representing overage type
    /// </summary>
    public enum Overage
    {
        /// <summary>
        /// No overage
        /// </summary>
        NO_OVERAGE = 0,
        /// <summary>
        /// Overage at face edge
        /// </summary>
        FACE_EDGE = 1,
        /// <summary>
        /// Overage goes on next face
        /// </summary>
        NEW_FACE = 2
    }
}
